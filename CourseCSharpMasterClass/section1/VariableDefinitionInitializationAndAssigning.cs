namespace CourseCSharpMasterClass.section1;

public class VariableDefinitionInitializationAndAssigning
{
    public static void Run()
    {
        // defining or setting up a variable
        string petsName;
        petsName = "Buddy";
        Console.WriteLine($"My pets name is {petsName}");
        
        // assigning a new value to the variable
        petsName = "Buddy";
        Console.WriteLine($"My pets name is {petsName}");
        Console.ReadKey();
    } 
}
